// Math

// 1) Math.abs()
console.log(Math.abs(2)) // 2
console.log(Math.abs(-2)) // 2


// 2) Math.ceil()
console.log(Math.ceil(3.141592)); // 4


// 3) Math.floor()
console.log(Math.floor(3.141592)); // 3


// 4) Math.max()
console.log(Math.max(1, 22, 38, 192)); // 192
const arr = [1, 22, 38, 192];
console.log(Math.max(arr));    // NaN
console.log(Math.max(...arr)); // 192


// 5) Math.min()
console.log(Math.min(1, 22, 38, 192)); // 1
console.log(Math.min(arr));    // NaN
console.log(Math.min(...arr)); // 1


// 6) Math.pow()
console.log(Math.pow(2, 3)); // 8
console.log(Math.pow(4, 2)); // 16
console.log(2 ** 3); // 8
console.log(4 ** 2); // 16


// 7) Math.random()
console.log(Math.random()); // 0 <= < 1


// 8) Math.round()
console.log(Math.round(3.141592)); // 3
console.log(Math.round(3.545213)); // 4






